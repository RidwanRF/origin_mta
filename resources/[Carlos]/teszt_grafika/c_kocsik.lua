local kocsik = false
function kocsikresi()
    
        kocsik = not kocsik
        if kocsik == true then
            outputChatBox ("Kikapcsoltad a kocsik modólását",0, 150,120, false)
			setTimer (StopVehMods, 1000, 1)
        else
            outputChatBox ("Bekapcsoltad a kocsik modólását", 100, 100, 0, false)
			setTimer (StartVehMods, 1000, 1)
        end
		end  
addCommandHandler("kocsik",kocsikresi, false)